create index if not exists sectors_gate_geom_idx on public.sectors using gist (gate_geometry_geom);
create index if not exists telemetry_points_geom_idx on public.telemetry_points using gist (position_geom);

create or replace function public.sync_route_template_geometries()
returns trigger
language plpgsql
as $$
begin
  new.raw_geometry_geom = public.geojson_to_linestring(new.raw_geometry);
  new.snapped_geometry_geom = public.geojson_to_linestring(new.snapped_geometry);
  new.start_finish_gate_geom = public.geojson_to_linestring(new.start_finish_gate);
  return new;
end;
$$;

create or replace function public.sync_sector_geometries()
returns trigger
language plpgsql
as $$
begin
  new.gate_geometry_geom = public.geojson_to_linestring(new.gate_geometry);
  return new;
end;
$$;

create or replace function public.sync_telemetry_geometries()
returns trigger
language plpgsql
as $$
begin
  new.position_geom = public.telemetry_to_point(new.latitude, new.longitude);
  return new;
end;
$$;

