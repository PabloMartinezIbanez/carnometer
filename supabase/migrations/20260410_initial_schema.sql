create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create or replace function public.geojson_to_linestring(payload jsonb)
returns geometry(LineString, 4326)
language sql
immutable
as $$
  select case
    when payload is null then null
    else st_setsrid(st_geomfromgeojson(payload::text), 4326)::geometry(LineString, 4326)
  end;
$$;

create or replace function public.telemetry_to_point(lat double precision, lon double precision)
returns geometry(Point, 4326)
language sql
immutable
as $$
  select st_setsrid(st_makepoint(lon, lat), 4326)::geometry(Point, 4326);
$$;

create table if not exists public.route_templates (
  id uuid primary key,
  user_id uuid not null default auth.uid() references auth.users (id) on delete cascade,
  install_id text not null,
  name text not null,
  is_closed boolean not null default false,
  raw_geometry jsonb not null,
  raw_geometry_geom geometry(LineString, 4326),
  snapped_geometry jsonb,
  snapped_geometry_geom geometry(LineString, 4326),
  start_finish_gate jsonb not null,
  start_finish_gate_geom geometry(LineString, 4326),
  notes text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.sectors (
  id uuid primary key,
  route_template_id uuid not null references public.route_templates (id) on delete cascade,
  user_id uuid not null default auth.uid() references auth.users (id) on delete cascade,
  display_order integer not null,
  label text not null,
  gate_geometry jsonb not null,
  gate_geometry_geom geometry(LineString, 4326),
  direction_hint text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.session_runs (
  id uuid primary key,
  user_id uuid not null default auth.uid() references auth.users (id) on delete cascade,
  route_template_id uuid not null references public.route_templates (id) on delete cascade,
  install_id text not null,
  status text not null check (status in ('draft', 'recording', 'completed', 'synced')),
  started_at timestamptz not null,
  ended_at timestamptz not null,
  distance_m double precision not null,
  max_speed_kmh double precision not null,
  avg_speed_kmh double precision not null,
  lap_summaries jsonb not null default '[]'::jsonb,
  sector_summaries jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.telemetry_points (
  id bigint generated by default as identity primary key,
  user_id uuid not null default auth.uid() references auth.users (id) on delete cascade,
  session_run_id uuid not null references public.session_runs (id) on delete cascade,
  timestamp timestamptz not null,
  latitude double precision not null,
  longitude double precision not null,
  position_geom geometry(Point, 4326),
  speed_mps double precision not null,
  accuracy_m double precision not null,
  heading_deg double precision not null,
  altitude_m double precision,
  created_at timestamptz not null default timezone('utc', now())
);

